import axios from 'axios';

export const createBoat = ({
  ownerId,
  photos,
  title,
  subtitle,
  boatType,
  size,
  crew,
  withCaptain,
  city,
  beach,
  description,
  rentBy,
  price,
  minimumTime,
  damageDeposit,
  sailors,
  includesFood,
  includesDrinks,
  bathrooms,
  bedrooms,
  hasKitchen,
}) =>
  axios.post('/api/boats', {
    ownerId,
    photos,
    title,
    subtitle,
    boatType,
    size,
    crew,
    withCaptain,
    city,
    beach,
    description,
    rentBy,
    price,
    minimumTime,
    damageDeposit,
    sailors,
    includesFood,
    includesDrinks,
    bathrooms,
    bedrooms,
    hasKitchen,
  });
